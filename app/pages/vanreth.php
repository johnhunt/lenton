<h1>Vanreth</h1>

<p>Vanreth was a medieval, fantasy, multiplayer web-based game in which you are the ruler of your own community. Every 10 minutes you could collect the tax from your citizens and use that money to buy items in-game. If you gathered the correct resources you could upgrade your community to increase its citizen capacity.</p>

